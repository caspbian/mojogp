def _get_diagonal_value_expr(kernel: IRKernel) -> str:
    """Get the diagonal value expression for K(x, x).

    For stationary kernels, K(x,x) = outputscale (dist_sq=0 → exp(0)=1).
    For non-stationary kernels, we need to evaluate the forward expression
    with dist_sq=0 and dot_prod=||x||^2.

    For simplicity, return the outputscale param (p[last]) for stationary,
    and Float32(1.0) as a safe default for non-stationary.
    """
    # The last param is typically outputscale for standard kernels
    # For composites this is approximate but safe (preconditioner uses it as hint)
    if kernel.num_params > 0:
        return f"p[{kernel.num_params - 1}]"
    return "Float32(1.0)"


