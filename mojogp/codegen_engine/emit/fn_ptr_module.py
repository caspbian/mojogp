def emit_fn_ptr_module(
    kernel: IRKernel,
    schedule: ScheduleConfig,
    module_name: str = "jit_kernel",
    dim: int = 0,
) -> str:
    """Generate complete .mojo source for a fn-ptr kernel module.

    The generated module exports init_provider() and materialize() Python
    bindings. init_provider() returns a dict of function pointer addresses
    that the JIT engine .so uses to call kernel operations.

    Args:
        kernel: Optimized IRKernel from the codegen pipeline
        schedule: GPU execution schedule (TM, shmem, ncols, block_size)
        module_name: Name for the generated Python module
        dim: Input dimension (uses kernel.dim if 0)

    Returns:
        Complete .mojo source code string ready for compilation
    """
    if dim == 0:
        dim = kernel.dim
    num_params = kernel.num_params
    block_size = schedule.block_size
    ncols_list = schedule.ncols
    splitj_default = schedule.splitj_forward
    is_ard = any(str(name).rsplit("_", 1)[-1].isdigit() for name in kernel.param_names)

    parts = []

    # 1. Header + imports
    parts.append(_emit_header(kernel, dim, num_params, block_size, module_name))

    # 2. GPU kernels
    parts.append("# " + "=" * 77)
    parts.append("# GPU Kernels (auto-generated by codegen_engine)")
    parts.append("# " + "=" * 77)

    parts.append(emit_forward_matvec(kernel, schedule))
    parts.append(emit_forward_matvec_splitj(kernel, schedule))
    parts.append(emit_gradient_matvec(kernel, schedule))
    parts.append(emit_single_gradient_matvec(kernel, schedule))
    parts.append(emit_cross_matvec(kernel, schedule))
    parts.append(emit_extract_diagonal(kernel, schedule))
    parts.append(emit_fill_kernel_matrix(kernel, schedule))
    parts.append(emit_fill_cross_covariance(kernel, schedule))
    parts.append(emit_noise_matvec(kernel, schedule))
    parts.append(emit_kronecker_forward_matvec(kernel, schedule))
    parts.append(emit_kronecker_gradient_matvec(kernel, schedule))
    parts.append(emit_mixed_forward_matvec(kernel, schedule))
    parts.append(emit_mixed_gradient_matvec(kernel, schedule))
    parts.append(emit_mixed_cross_matvec(kernel, schedule))
    parts.append(emit_mixed_materialize(kernel, schedule))
    parts.append(emit_mixed_extract_diagonal(kernel, schedule))

    # 3. Provider struct
    parts.append(_emit_provider_struct(num_params))

    # 4. Fn-ptr implementations
    parts.append(
        _emit_fn_ptr_implementations(
            kernel, dim, num_params, block_size, ncols_list, schedule.tm, splitj_default
        )
    )

    # 5. Fn-ptr type aliases + Python bindings
    parts.append(_emit_helpers_and_bindings(num_params, module_name, is_ard))

    return "\n".join(parts)


