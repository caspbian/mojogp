        self.diagonal_val = Float32(1.0)
        self.k_matrix_owner = UnsafePointer[DeviceBuffer[DType.float32], MutAnyOrigin]()
        self.k_matrix_ptr = UnsafePointer[Float32, MutAnyOrigin]()
        self.is_materialized = False
        self.materialization_mode = 0
        self.materialized_corr_ptr = UnsafePointer[Float32, MutAnyOrigin]()
